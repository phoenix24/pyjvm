public class SampleInvoke2 {

  public static double add(double i, double k) {
    return i + k;
  }

  public static double sub(double i, double k) {
    return i - k;
  }

  public static double mul(double a, double b) {
    return a * b;
  }

  public static double div(double a, double b) {
    return a / b;
  }

  public static double rem(double a, double b) {
    return a % b;
  }

  public static double neg(double a) {
    return -a;
  }

  public static double inc1(double a) {
    return a++;
  }

  public static double inc2(double a) {
    return ++a;
  }
}
